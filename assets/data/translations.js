  portName(name) {
    if (!name) return name;
    // 1. Exact match
    if (this.portNameKo[name]) return this.portNameKo[name];
    // 2. Strip parentheticals: "San Juan (Puerto Rico)" → "San Juan"
    const noParens = name.replace(/\s*\([^)]*\)/g, '').trim();
    if (noParens !== name && this.portNameKo[noParens]) return this.portNameKo[noParens];
    // 3. Strip after comma: "Miami, Florida" → "Miami"
    const beforeComma = name.split(',')[0].trim();
    if (beforeComma !== name && this.portNameKo[beforeComma]) return this.portNameKo[beforeComma];
    // 4. Strip accent marks for Málaga→Malaga fallback
    const noAccent = name.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    if (noAccent !== name && this.portNameKo[noAccent]) return this.portNameKo[noAccent];
    return name;
  },
